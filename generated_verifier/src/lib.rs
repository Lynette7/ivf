        /// This should convert G1Point to bytes format for precompile calls
        /// The format should look like [x(32 bytes), y(32 bytes)] = 64 bytes total
        fn g1_point_to_bytes(&self, point: &G1Point) -> [u8; 64] {
            let mut bytes = [0u8; 64];
            let x_bytes = to_bytes_be(point.x);
            let y_bytes = to_bytes_be(point.y);
            bytes[0..32].copy_from_slice(&x_bytes);
            bytes[32..64].copy_from_slice(&y_bytes);
            bytes
        }
        /// This should converts bytes to a G1Point
        /// We expect a format like this 64 bytes: [x (32 bytes), y (32 bytes)]
        fn bytes_to_g1_point(&self, bytes: &[u8]) -> Option<G1Point> {
            if bytes.len() < 64 {
                return None;
            }
            Some(G1Point {
                x: from_bytes_be(bytes[0..32].try_into().map_err(|_| ()).ok()?),
                y: from_bytes_be(bytes[32..64].try_into().map_err(|_| ()).ok()?),
            })
        }

